# creating a dictionary
dict = {"name": "Udaya",
        "age": 25,
        "city": "Gampaha",
        "country": "Sri Lanka"
        }

# accessing the values
print(dict["name"])

#adding new values to the dictionary (key-value pairs)
dict["Uni"] = "Esoft"

#modifying the values(modifying the key-value pairs)
dict["age"] = 26

#removing the values(removing the key-value pairs)
del dict["country"]

#printing the dictionary
print(dict)

