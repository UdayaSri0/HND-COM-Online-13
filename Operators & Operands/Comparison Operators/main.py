A = "5"
B = "10"

# == equal to
print('A == B: ', A == B) # Output False. 10 is not equal to 5.

#!= not equal to
print('A != B: ', A!= B) # Output True. 10 is not equal to 5.

# > greater than
print('A > B: ', A > B) # output True. 10 is greater than 5

# < less than
print('A < B: ', A < B) # 10 is less than 5

# >= Greater than or equal to
print('A >= B: ', A >= B) # 10 is Greater than or equal to 5 

# <= Less tha or equal to
print('A <= B: ', A <= B) # 10 is less than or equal  to 5




